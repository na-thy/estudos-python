'''Faça um Programa que peça as 4 notas bimestrais e mostre a média.'''

n1 = eval(input('Digite o valor da primeira nota: '))
n2 = eval(input('Digite o valor da segunda nota: '))
n3 = eval(input('Digite o valor da terceira nota: '))
n4 = eval(input('Digite o valor da quarta nota: '))

media = (n1+n2+n3+n4)/4

print('A média do aluno foi', media)